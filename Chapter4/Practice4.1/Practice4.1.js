//Create a variable with a Boolean value
let myVariable = false;

//Output the value of the variable to the console.
console.log(myVariable);

//Check whether the variable is true and if so
//Output a message
if (myVariable) {
    console.log(`My variable is ${myVariable}`);
}

//Add another if statement with and ! in front of the variable to 
