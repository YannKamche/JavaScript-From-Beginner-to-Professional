/* Strings
A string is used to store a text value. It is a sequence of 
characters
- Double quotes e.g let myActivity = "Let's learn JavaScript"
- Single quotes e.g let myOtherActivity = 'Le
- Backsticks
*/

//Double quotes
let doubleQuoteVariable = "Let's learn JavaScript";
console.log(doubleQuoteVariable);

//Single quotes
let singleQuoteVariable = 'I am learning JavaScript';
console.log(singleQuoteVariable);

//Backsticks
let language = "JavaScript";
let message = `Let's learn ${language}`;
console.log(message);
